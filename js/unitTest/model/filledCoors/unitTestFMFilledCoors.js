    const _unitTest = (difficulty, baseArgs, expectedFilledCoors) => {
        console.info("UnitTestFMFilledCoors _unitTest");
        console.info(`difficulty: ${difficulty}`);
        console.info("expectedFilledCoors: " +
                JSON.stringify(expectedFilledCoors));
        const charNo = FMLAS.l(difficulty);
        const filledCoorNo = charNo * ((charNo - 1) / 2);
        const filledCoors = FM_FILLED_COORS.bind(this, difficulty,
                filledCoorNo).apply(this, _filledCoorArgs(baseArgs).concat(
                [1])); // charValSpacing doesn't matter in swapping portions
        console.info(`filledCoors: ${JSON.stringify(filledCoors)}`);
        SHOW_RESULT_MSGS(
                _failMsgs(expectedFilledCoors, filledCoorNo, filledCoors));
    }; // _unitTest

    /**
     * Pure Function
     * @author DoubleX @since v1.0 @version v1.0
     * @param {Array[Number]} baseArgs - The building block of the argument list
     * @returns {Array[]} The requested list of arguments
     */
    const _filledCoorArgs = baseArgs => {
        const portionInGroupArgs = Array(baseArgs.length).fill(baseArgs);
        return [
            baseArgs,
            baseArgs,
            portionInGroupArgs,
            portionInGroupArgs
        ];
    }; // _filledCoorArgs

    /**
     * Pure Function
     * @author DoubleX @since v1.0 @version v1.0
     * @param {Array[Object[Number, Number]]} expectedFilledCoors - The expected
     *                                                              results
     * @param {Number} filledCoorNo - The number of coordinates to be filled
     * @param {Array[Object[Number, Number]]} filledCoors - The results to be
     *                                                      tested against
     * @returns {Array[String]} The requested list of fail messages
     */
    const _failMsgs = (expectedFilledCoors, filledCoorNo, filledCoors) => {
        if (!filledCoors) return ["filledCoors doesn't exist!"];
        if (filledCoors.constructor !== Array) {
            return ["filledCoors isn't an Array!"];
        }
        const failMsgs = [], filledCoorLength = filledCoors.length;
        if (filledCoorLength !== filledCoorNo) {
            failMsgs.push("The number of filled coordinates should be " +
                    `${filledCoorNo} but is instead ${filledCoorLength}!`);
        }
        return filledCoors.reduce(
                _failCoorMsgs.bind(this, expectedFilledCoors), failMsgs);
    }; // _failMsgs

    /**
     * Pure Function
     * @author DoubleX @since v1.0 @version v1.0
     * @param {Array[Object[Number, Number]]} expectedFilledCoors - The expected
     *                                                              results
     * @param {Array[String]} accumFailMsgs - The accumulated list of fail
     *                                        messages
     * @param {Object[Number, Number]} filledCoor - The coordinate to be tested
     *                                              against
     * @param {Number} i - The index of the coordinate to be tested against
     * @returns {Array[String]} The requested list of fail messages
     */
    const _failCoorMsgs = (
             expectedFilledCoors, accumFailMsgs, filledCoor) => {
        // The orderings among the filled coordinates don't matter at all
        return expectedFilledCoors.some(_isSameCoor.bind(this, filledCoor)) ?
                accumFailMsgs : accumFailMsgs.concat(["The filled " +
                        `coordinates ${JSON.stringify(filledCoor)} should be` +
                        ` within ${JSON.stringify(expectedFilledCoors)} but ` +
                        "instead isn't!"]);
        //
    }; // _failCoorMsgs

    /**
     * Pure Function
     * @author DoubleX @since v1.0 @version v1.0
     * @param {Object[Number, Number]} coor1 - A coordinate to be compared
     *                                         against
     * @param {Object[Number, Number]} coor2 - A coordinate to be compared
     *                                         against
     * @returns {Boolean} The check result
     */
    const _isSameCoor = (coor1, coor2) => {
        return coor1.rowIndex === coor2.rowIndex &&
                coor1.colIndex === coor2.colIndex;
    }; // _isSameCoor

